def carga_datos_ventas():
    cod_venta=[]
    pro_vendido=[]
    cant_vendida=[]
    cont=0
    continuar="s"
    while continuar=="s":
        cont=cont+1
        cod_venta.append(cont)
        pro=input("Ingrese el Articulo vendido: ")
        pro_vendido.append(pro)
        cant=int(input("Ingrese la cantidad vendida del articulo: "))
        cant_vendida.append(cant)
        continuar=input("Ingresar\ns=si desea continuar la carga\nn=si finaliza la carga\nOpci√≥n: ")
        if continuar=="s":
            continuar=="s"
        else:
            archivo_ventas(pro_vendido,cant_vendida)
            limpiar()
            print("Finalizo la carga de ventas realizadas")

